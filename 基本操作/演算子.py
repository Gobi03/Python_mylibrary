a ** b        # a の b 乗
a // b        # 切り捨て除算

~a            # ビット反転
a & b         # AND:論理積(aもbも1のビットが1)
a | b         # OR:論理和(aまたはbが1のビットが1)
a ^ b         # XOR:排他的論理和(aまたはbが1のビットが1)
a << b        # b ビット左シフト
a >> b        # b ビット右シフト

a in b           # a が b に含まれる
a not in b       # a が b に含まれない

# ブール演算子(and, or, not)
a and b         # a も b も真であれば真
a or b          # a または b が真であれば真
not a           # a が偽であれば真

# 条件演算(if else)
x if c else y   # c が真であれば x を、さもなくば y を返します。

